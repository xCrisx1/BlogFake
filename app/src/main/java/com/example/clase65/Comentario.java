package com.example.clase65;

public class Comentario {
    public String Nombre;
    public String Titulo;
    public String Texto;
    public int imgID;
    public int ID;

    public Comentario(String nombre, String titulo, String texto, int imgid, int id){
        this.Nombre = nombre;
        this.Texto = texto;
        this.Titulo = titulo;
        this.imgID = imgid;
        this.ID = id;
    }
}
